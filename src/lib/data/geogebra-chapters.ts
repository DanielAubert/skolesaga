// Helper function to get all chapters flat
export const getAllGeoGebraChapters = (): GeoGebraChapter[] => {
  return GEOGEBRA_COURSES.flatMap(course => course.chapters);
};

// Helper function to get course by id
export const getGeoGebraCourse = (id: string): GeoGebraCourse | undefined => {
  return GEOGEBRA_COURSES.find(course => course.id === id);
};

// Helper function to get chapter by id
export const getGeoGebraChapter = (id: string): GeoGebraChapter | undefined => {
  return getAllGeoGebraChapters().find(chapter => chapter.id === id);
};

